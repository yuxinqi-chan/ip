export function detectIPVersion(ip: string) {
  if (
    /^(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$/.test(
      ip
    )
  ) {
    return "IPv4";
  } else if (
    /^(([0-9a-fA-F]{1,4}:){7,7}[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,7}:|([0-9a-fA-F]{1,4}:){1,6}:[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,5}(:[0-9a-fA-F]{1,4}){1,2}|([0-9a-fA-F]{1,4}:){1,4}(:[0-9a-fA-F]{1,4}){1,3}|([0-9a-fA-F]{1,4}:){1,3}(:[0-9a-fA-F]{1,4}){1,4}|([0-9a-fA-F]{1,4}:){1,2}(:[0-9a-fA-F]{1,4}){1,5}|[0-9a-fA-F]{1,4}:((:[0-9a-fA-F]{1,4}){1,6})|:((:[0-9a-fA-F]{1,4}){1,7}|:)|fe80:(:[0-9a-fA-F]{0,4}){0,4}%[0-9a-zA-Z]{1,}|::(ffff(:0{1,4}){0,1}:){0,1}((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])|([0-9a-fA-F]{1,4}:){1,4}:((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9]))$/.test(
      ip
    )
  ) {
    return "IPv6";
  }
}

export function getIpInfo(ip?: string, cf?: CfProperties<unknown>) {
  const clientIP = ip || "Unknown";
  const ipVersion = detectIPVersion(clientIP) || "Unknown";
  const country = cf?.country || "Unknown";
  const city = cf?.city || "Unknown";
  const region = cf?.region || "Unknown";
  const timezone = cf?.timezone || "Unknown";
  const asOrganization = cf?.asOrganization || "Unknown";
  const asn = cf?.asn || "Unknown";
  const latitude = cf?.latitude || 0;
  const longitude = cf?.longitude || 0;

  const data = {
    ip: clientIP,
    ipVersion: ipVersion,
    country: country as string,
    city: city,
    region: region,
    timezone: timezone,
    isp: asOrganization,
    asn: asn,
    latitude: latitude,
    longitude: longitude,
  };
  return data;
}

export function getFlagEmoji(countryCode: string) {
  const codePoints = countryCode
    .toUpperCase()
    .split("")
    .map((char) => 127397 + char.charCodeAt(0));
  return String.fromCodePoint(...codePoints);
}
